// Operadores lógicos (logical operators)

// - 2 valores booleanos, quando verificados, resultará em verdadeiro ou falso

let pao = false;
let queijo = false;

// AND &&
if(pao && queijo) console.log('Tem pão E queijo')

// OR ||
if(pao || queijo) console.log('Tem pão OU queijo')

// NOT !
if(!pao) console.log('Não tem pão')
